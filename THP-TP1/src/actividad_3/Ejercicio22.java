package actividad_3;

public class Ejercicio22 {

	public static void main(String[] args) {
		int value = 1;
		
		while (value <= 5) {
			System.out.print(value + " ");
			value++;
		}
		value = value - 1;
		System.out.println();
		while (value >= 1) {
			System.out.print(value + " ");
			value--;
		}

	}

}
